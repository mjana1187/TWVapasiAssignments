package prog.Assignment.JavaLevel1.Operators;

public class TrianglePerimeter {
    //This program is to calculate the PERIMETER of a Triangle
    public static void main(String[] args) {

        int iS1 = 2;
        int iS2 = 3;
        int iS3 = 5;

        //calculate perimeter
        int iPerimeter =  iS1 + iS2 + iS3;

        //Print the Area and perimeter result
        System.out.println("This program is to calculate PERIMETER of a Triangle with Sides " +iS1+
                " , "+ iS2 + " , " + iS3 +  " is " +iPerimeter +".");
    }
}
